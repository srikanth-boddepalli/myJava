class  printingSquareAndCubeUseingMethods
{
	public static void main(String[] args) 
	{
		System.out.println(square(5));
		System.out.println(cube(5));
	}
	public static int square(int a)
	{
		int b = a*a ;
        return b ;
	}
	public static int cube(int c)
	{
		int d = c*c*c ;
		return d ;
	}

}
